    const calorieGoalInput = document.getElementById('calorie-goal');
    const proteinGoalInput = document.getElementById('protein-goal');
    const foodTableBody = document.querySelector('#food-table tbody');
    const remainingCaloriesElem = document.getElementById('remaining-calories');
    const remainingProteinElem = document.getElementById('remaining-protein');
    const consumedList = document.getElementById('consumed-list');
    
    let calorieGoal = 0;
    let proteinGoal = 0;
    let remainingCalories = 0;
    let remainingProtein = 0;
    
    function populateTable() {
        foodData.forEach(food => {
            const row = document.createElement('tr');
    
            const nameCell = document.createElement('td');
            nameCell.textContent = food.name;
            row.appendChild(nameCell);
    
            const typeCell = document.createElement('td');
            typeCell.textContent = food.type;
            row.appendChild(typeCell);
    
            const servingSizeCell = document.createElement('td');
            servingSizeCell.textContent = `${food.serving_size_g} g`;
            row.appendChild(servingSizeCell);
    
            const proteinServingCell = document.createElement('td');
            proteinServingCell.textContent = `${food.protein_per_serving_g} g`;
            row.appendChild(proteinServingCell);
    
            const caloriesServingCell = document.createElement('td');
            caloriesServingCell.textContent = `${food.calories_per_serving} kcal`;
            row.appendChild(caloriesServingCell);
    
            const actionCell = document.createElement('td');
            const addButton = document.createElement('button');
            addButton.textContent = 'Add';
            addButton.addEventListener('click', () => addFood(food));
            actionCell.appendChild(addButton);
            row.appendChild(actionCell);
    
            foodTableBody.appendChild(row);
        });
    }
    
    function addFood(food) {
        const message = `Please enter the quantity in grams for ${food.name}. A typical serving size is ${food.serving_size_g} grams.`;
        const quantityInput = prompt(message);
    
        if (quantityInput === null) {
            return; // User canceled
        }
    
        const quantity = parseFloat(quantityInput);
    
        if (isNaN(quantity) || quantity <= 0) {
            alert('Please enter a valid quantity.');
            return;
        }
    
        const calories = (food.calories_per_100g / 100) * quantity;
        const protein = (food.protein_per_100g / 100) * quantity;
    
        remainingCalories -= calories;
        remainingProtein -= protein;
    
        const listItem = document.createElement('li');
        listItem.textContent = `${food.name} - ${quantity} grams (Calories: ${calories.toFixed(1)}, Protein: ${protein.toFixed(1)}g)`;
        consumedList.appendChild(listItem);
    
        updateResults();
    }
    
    function updateResults() {
        remainingCaloriesElem.textContent = `Remaining Calories: ${remainingCalories.toFixed(1)}`;
        remainingProteinElem.textContent = `Remaining Protein: ${remainingProtein.toFixed(1)} g`;
    }
    
    document.getElementById('set-goals').addEventListener('click', () => {
        calorieGoal = parseFloat(calorieGoalInput.value);
        proteinGoal = parseFloat(proteinGoalInput.value);
    
        if (isNaN(calorieGoal) || isNaN(proteinGoal) || calorieGoal <= 0 || proteinGoal <= 0) {
            alert('Please set valid calorie and protein goals.');
            return;
        }
    
        remainingCalories = calorieGoal;
        remainingProtein = proteinGoal;
        updateResults();
    });
    
    // Populate the table on load
    populateTable();
    
